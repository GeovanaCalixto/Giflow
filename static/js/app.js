document.addEventListener("DOMContentLoaded", () => {
  const form = document.querySelector("#assistant-form");
  const input = document.querySelector("#assistant-input");
  const messages = document.querySelector("#chat-messages");
  const aiTrigger = document.querySelector("#ai-trigger");
  const openInline = document.querySelector("#open-ai-inline");
  const assistantWindow = document.querySelector("#assistant");
  const closeAi = document.querySelector("#close-ai");
  const modeButtons = document.querySelectorAll(".mode-btn");

  let currentMode = "bilingual";

  function openAssistant() {
    assistantWindow.classList.remove("hidden");
    input.focus();
  }

  function closeAssistant() {
    assistantWindow.classList.add("hidden");
  }

  function addMsg(type, text) {
    const msg = document.createElement("div");
    msg.className = type === "user" ? "user-message" : "bot-message";
    msg.innerHTML = text;
    messages.appendChild(msg);
    messages.scrollTop = messages.scrollHeight;
  }

  function clean(text) {
    return text.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "");
  }

  function formatList(items) {
    return items.map((item) => `• ${item}`).join("<br>");
  }

  function formatAnswer(answer) {
    if (typeof answer === "string") {
      return answer;
    }

    if (currentMode === "english") {
      return answer.en;
    }

    if (currentMode === "portuguese") {
      return answer.pt;
    }

    if (currentMode === "translate") {
      return `<strong>English:</strong><br>${answer.en}<br><br><strong>Português:</strong><br>${answer.pt}`;
    }

    return `<strong>English:</strong><br>${answer.en}<br><br><strong>Português:</strong><br>${answer.pt}`;
  }

  function makeAnswer(en, pt) {
    return { en, pt };
  }

  function storyAnswer(story) {
    return makeAnswer(
      `<strong>${story.id} story:</strong><br>${story.en}<br><br><strong>STAR structure:</strong><br>Situation: explain the context.<br>Task: clarify your responsibility.<br>Action: show what you did.<br>Result: close with impact and learning.`,
      `<strong>História de ${story.id}:</strong><br>${story.pt}<br><br><strong>Estrutura STAR:</strong><br>Situação: explique o contexto.<br>Tarefa: esclareça sua responsabilidade.<br>Ação: mostre o que você fez.<br>Resultado: feche com impacto e aprendizado.`
    );
  }

  function listAnswer(titleEn, titlePt, listEn, listPt) {
    return makeAnswer(
      `<strong>${titleEn}:</strong><br>${formatList(listEn)}`,
      `<strong>${titlePt}:</strong><br>${formatList(listPt)}`
    );
  }

  function buildResponse(rawQuery) {
    const query = clean(rawQuery);

    const storyMatch = giFlowData.stories.find((story) =>
      story.keywords.some((keyword) => query.includes(clean(keyword)))
    );

    if (storyMatch) return storyAnswer(storyMatch);

    if (query.includes("short version") || query.includes("versao curta") || query.includes("versão curta")) {
      return makeAnswer(
        `<strong>Short version:</strong><br>${giFlowData.profile.narratives.short.en}`,
        `<strong>Versão curta:</strong><br>${giFlowData.profile.narratives.short.pt}`
      );
    }

    if (query.includes("medium version") || query.includes("middle version") || query.includes("versao media") || query.includes("versão média")) {
      return makeAnswer(
        `<strong>Medium version:</strong><br>${giFlowData.profile.narratives.medium.en}`,
        `<strong>Versão média:</strong><br>${giFlowData.profile.narratives.medium.pt}`
      );
    }

    if (query.includes("long version") || query.includes("versao longa") || query.includes("versão longa")) {
      return makeAnswer(
        `<strong>Long version:</strong><br>${giFlowData.profile.narratives.long.en}`,
        `<strong>Versão longa:</strong><br>${giFlowData.profile.narratives.long.pt}`
      );
    }

    if (query.includes("about me") || query.includes("sobre mim") || query.includes("narrative") || query.includes("narrativa")) {
      return makeAnswer(
        `<strong>About me narrative:</strong><br>${giFlowData.profile.narratives.medium.en}`,
        `<strong>Narrativa sobre mim:</strong><br>${giFlowData.profile.narratives.medium.pt}`
      );
    }

    if (query.includes("tell me about") || query.includes("about yourself") || query.includes("intro") || query.includes("apresentacao") || query.includes("apresentação")) {
      return giFlowData.interviewBank.tell_me_about_yourself;
    }

    if (query.includes("experience") || query.includes("experiencia") || query.includes("experiência") || query.includes("background")) {
      return giFlowData.interviewBank.experience;
    }

    if (query.includes("google") || query.includes("why google") || query.includes("por que google")) {
      return giFlowData.interviewBank.why_google;
    }

    if (query.includes("web3") || query.includes("mba")) {
      return giFlowData.interviewBank.why_web3;
    }

    if (query.includes("dao") || query.includes("youtube") || query.includes("opportunity") || query.includes("oportunidade")) {
      return giFlowData.interviewBank.dao_lite;
    }

    if (query.includes("fipan") || query.includes("challenge") || query.includes("desafio")) {
      return giFlowData.interviewBank.fipan;
    }

    if (query.includes("unico roteiro") || query.includes("único roteiro") || query.includes("roteiro")) {
      return giFlowData.interviewBank.unico_roteiro;
    }

    if (query.includes("social impact") || query.includes("volunteer") || query.includes("ngo") || query.includes("rs") || query.includes("rio grande") || query.includes("impacto social")) {
      return giFlowData.interviewBank.social_impact;
    }

    if (query.includes("startup") || query.includes("ambiguity") || query.includes("ambiguidade")) {
      return giFlowData.interviewBank.startup;
    }

    if (query.includes("weakness") || query.includes("fraqueza") || query.includes("ponto fraco") || query.includes("development") || query.includes("desenvolvimento")) {
      return giFlowData.interviewBank.weakness;
    }

    if (query.includes("strength") || query.includes("forte") || query.includes("qualidade") || query.includes("pontos fortes")) {
      return makeAnswer(
        `<strong>Core strengths:</strong><br>${formatList(giFlowData.profile.strengths.en)}<br><br>${giFlowData.interviewBank.strengths.en}`,
        `<strong>Pontos fortes:</strong><br>${formatList(giFlowData.profile.strengths.pt)}<br><br>${giFlowData.interviewBank.strengths.pt}`
      );
    }

    if (query.includes("english") || query.includes("ingles") || query.includes("inglês")) {
      return giFlowData.interviewBank.english;
    }

    if (query.includes("learn") || query.includes("learning") || query.includes("aprender") || query.includes("aprendizado")) {
      return giFlowData.interviewBank.learning;
    }

    if (query.includes("connector") || query.includes("connect") || query.includes("vocab") || query.includes("vocabulary") || query.includes("conector")) {
      return listAnswer("Useful connectors", "Conectores úteis", giFlowData.dictionary.connectors.en, giFlowData.dictionary.connectors.pt);
    }

    if (query.includes("verb") || query.includes("impact")) {
      return listAnswer("Impact verbs", "Verbos de impacto", giFlowData.dictionary.verbs.en, giFlowData.dictionary.verbs.pt);
    }

    if (query.includes("travou") || query.includes("blank") || query.includes("rephrase") || query.includes("recover")) {
      return listAnswer("Recovery phrases", "Frases de recuperação", giFlowData.dictionary.recovery.en, giFlowData.dictionary.recovery.pt);
    }

    if (query.includes("star")) {
      return makeAnswer(
        "Use this STAR structure:<br><br><strong>Situation:</strong> give context.<br><strong>Task:</strong> explain your responsibility.<br><strong>Action:</strong> describe what you did.<br><strong>Result:</strong> show impact, learning, or measurable improvement.",
        "Use esta estrutura STAR:<br><br><strong>Situação:</strong> dê o contexto.<br><strong>Tarefa:</strong> explique sua responsabilidade.<br><strong>Ação:</strong> descreva o que você fez.<br><strong>Resultado:</strong> mostre impacto, aprendizado ou melhoria mensurável."
      );
    }

    return makeAnswer(
      "I can help with your profile, about me narrative, experience, Google, Web3, FIPAN, Único Roteiro, leadership, conflict, pressure, social impact, strengths, weaknesses, English practice, connectors, impact verbs, or STAR answers. Try asking: “Give me the long version about me” or “Help me answer my weaknesses.”",
      "Posso ajudar com seu perfil, narrativa sobre você, experiência, Google, Web3, FIPAN, Único Roteiro, liderança, conflito, pressão, impacto social, pontos fortes, fraquezas, prática de inglês, conectores, verbos de impacto ou respostas em STAR. Tente perguntar: “Me dê a versão longa sobre mim” ou “Me ajude a responder minhas fraquezas”."
    );
  }

  aiTrigger.addEventListener("click", openAssistant);
  openInline.addEventListener("click", openAssistant);
  closeAi.addEventListener("click", closeAssistant);

  modeButtons.forEach((button) => {
    button.addEventListener("click", () => {
      modeButtons.forEach((btn) => btn.classList.remove("active"));
      button.classList.add("active");
      currentMode = button.dataset.mode;

      const label = button.textContent;
      addMsg("bot", currentMode === "portuguese" ? `Modo alterado para <strong>${label}</strong>.` : `Mode changed to <strong>${label}</strong>.`);
    });
  });

  form.addEventListener("submit", (event) => {
    event.preventDefault();

    const question = input.value.trim();
    if (!question) return;

    addMsg("user", question);
    input.value = "";

    const response = buildResponse(question);

    window.setTimeout(() => {
      addMsg("bot", formatAnswer(response));
    }, 250);
  });
});
